package JavaClasses.day_04_Variables_Intro;

public class DeclarationTypes {
    public static void main(String[] args) {

        int bananas = 34;
        System.out.println(bananas);
        int students = 400;
        System.out.println(students+1);

        int peopleInCity, birdsOnTree, fishInLake;
        peopleInCity = 5000;
        birdsOnTree = 25;
        fishInLake = 200;
        System.out.println(peopleInCity);
        System.out.println(birdsOnTree);
        System.err.println(fishInLake);    // ---> "err" prints in RED color also prints in last order in console

        int studentsInVA = 58, studentsInNY = 65;
        System.out.println(studentsInVA);
        System.out.println(studentsInNY);


    }

}
