package JavaClasses.day_14_multi_branch_if_statements;

public class IfWithoutElse {
    public static void main(String[] args) {

        int year = 2020;
        if (year == 2020){
            System.out.println("Covid19 - Pandemic year!");
            System.out.println("Wear mask and stay away from me!");

        }
        System.out.println("Keep coding Java");
        String country = "USA";
        if (country.equals("USA")){
            System.out.println("Washington DC is capital of "+country);
            System.out.println("White house is on Pennsylvania ave");
            System.out.println("Welcome to "+country);
        }

    }
}
