package JavaBasics.day_05_primitives_concatanation;

public class WatchInfo {
    public static void main(String[] args){

        String brand = "Patek Philippe";
        String color = "DimondGold";
        double price = 380000;
        boolean waterResistant = true;
        boolean isSmart = false;
        char gender = 'M';
        System.out.println("Brand: \t\t\t"+brand);
        System.out.println("Color: \t\t\t"+color);
        System.out.println("Price: \t\t\t"+price);
        System.out.println("Waterproof: \t"+waterResistant);
        System.out.println("Smartwatch:\t\t"+isSmart);
        System.out.println("Gender: \t\t"+gender);

    }
}
