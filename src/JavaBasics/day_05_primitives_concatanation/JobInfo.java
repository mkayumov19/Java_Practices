package JavaBasics.day_05_primitives_concatanation;

public class JobInfo {
    public static void main(String[] args) {

        String title = "Java SDET";
        String company = "Palo Alto Inc";
        String jobDiscription = "Experience in Selenium, Java, Cucumber, Sandwich";
        double salary = 6.999;
        byte yearsOfExp = 5;
        boolean hasBenefits = true;

        System.out.println("Job title is:\t\t\t"+title);
        System.out.println("Company name:\t\t\t"+company);
        System.out.println("Job description is\t\t"+jobDiscription);
        System.out.println("Salary is:\t\t\t\t"+salary);
        System.out.println("Experienced needed:\t\t"+yearsOfExp);
        System.out.println("Job has benefits:\t\t"+hasBenefits);

    }
}
