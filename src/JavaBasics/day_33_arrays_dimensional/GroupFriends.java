package JavaBasics.day_33_arrays_dimensional;

public class GroupFriends {
    public static void main(String[] args) {
        String[] friends = {"Fikret","Aygun","Mariam","Aghabe","Halida","Subida"};
        for(String each : friends){
            System.out.println(each + " Happy Holidays!");
        }

    }
}
